void setup(){
	//    DDRD = 0b00000001;
	//FET 0..3 & LED 0...1
	PORTC.OUT = 0b00000000;
	PORTC.DIR = 0b11110011;

	//FET 4...11
	PORTD.OUT = 0b00000000; //ここを0にすると消える
	PORTD.DIR = 0b11111111; //1にすると出力できる

	//FET 12...15
	PORTE.OUT = 0b00000000;
	PORTE.DIR = 0b00001111; //出力
	
	//LED 2...7
	PORTB.OUT = 0x00;
	PORTB.DIR = 0b00111111;
	
	//LED 8...15
	PORTA.OUT = 0x00;
	PORTA.DIR = 0b11111111;
}

void lineDelay(){
//	_delay_ms(50);
	_delay_us(10);
}

//配線を最強にミスっていて LED 0から順番に PC1,0, PB5,4,3,2,1,0, PA7,6,5,4,3,2,1,0 となっている。

void dataChanger(uint16_t sdata[]){
	//しょうがないのでビットを入れ替える。
	//1個ずらし、２個ずらし、４個ずらしを使って目的の場所に入れ替えていく
	
	for(int i=0; i<16; i++){
		sdata[i] =	((sdata[i] & 0x5555) <<1 )
					| ((sdata[i] & 0xAAAA) >>1 );
		sdata[i] =  ((sdata[i] & 0x3300) <<2 )
					| ((sdata[i] & 0xCC00) >>2 )
					| (sdata[i] & 0x00FF);
		sdata[i] =  ((sdata[i] & 0x0F0C) <<4 )
					| ((sdata[i] & 0xF0C0) >>4 )
					| (sdata[i] & 0x0033);
	}
}

void show(uint16_t sdata[]){
	int i=0;
	uint8_t tmp;
	uint8_t data[16][3];
	
	for(int di=0; di<16; di++){
		data[di][0] = sdata[di] & 0x03;
		data[di][1] = (sdata[di] >> 2) & 0x3F;
		data[di][2] = (sdata[di] >> 8) & 0xFF;
	}
	
//	memset(data, 0, sizeof(data));
	
	//FET0...3 = PC4...7
	for(i=0; i<4; i++){
		tmp = 0x01<<(i + 4);
		PORTC.OUT = 0x00;	//FET
		PORTE.OUT = 0x00;	//FET
		PORTD.OUT = 0x00;	//FET
		PORTA.OUT = data[i][2];
		PORTB.OUT = data[i][1];
		PORTC.OUT = tmp | data[i][0];	//FET
		lineDelay();
	}
	
	//FET4...11 = PD0...7
	for(; i<8+4; i++){
		tmp = 0x01<<(i -4);
		PORTD.OUT = 0x00; //FET
		PORTC.OUT = data[i][0];	//FET
		PORTE.OUT = 0x00;	//FET
		PORTA.OUT = data[i][2];
		PORTB.OUT = data[i][1];
		PORTD.OUT = tmp;	//FET
		lineDelay();
	}
	
	//FET12...15 -> PE0...3
	for(; i<16; i++){
		tmp = 0x01<<(i -4 -8);
		PORTE.OUT = 0x00;	//FET
		PORTD.OUT = 0x00;	//FET
		PORTA.OUT = data[i][2];
		PORTB.OUT = data[i][1];
		PORTC.OUT = data[i][0];	//FET
		PORTE.OUT = tmp;	//FET
		lineDelay();
	}
	
}

